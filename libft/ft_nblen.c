#include "libft.h"

int		ft_nblen(int nb)
{
	int		len;

	len = 0;
	if (nb == 0)
		return (1);
	if (nb < 0)
	{
		len++;
		nb *= -1;
	}
	while (nb != 0)
	{
		nb /= 10;
		len++;
	}
	return (len);
}
